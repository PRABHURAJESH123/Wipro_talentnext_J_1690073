package mypack;

public class AchiiToChar {

	
		
		    public static void main(String[] args) {
		        // Initialize the integer array with ASCII values
		        int[] asciiArray = {65, 66, 67, 68, 69}; // A, B, C, D, E
		        
		        // Iterate through the array and print the corresponding character values
		        for (int asciiValue : asciiArray) {
		            // Convert the ASCII value to a character and print it
		            char character = (char) asciiValue;
		            System.out.print(character + " ");
		        }
		    }
		


	}


